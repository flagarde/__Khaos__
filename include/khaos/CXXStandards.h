#ifndef KHAOS_CXXSTANDARDS_H
#define KHAOS_CXXSTANDARDS_H

/*! \file CXXStandards.h
    \brief Define the version of the C++ standard.

    Define macros that expand to the integer constant of type \b long whose value are the version of the C++ standard.

    Official values for the \b __cplusplus macro.
*/

#define KHAOS_STANDARD_CXX97 199711L  //!< Value for the C++97 standard
#define KHAOS_STANDARD_CXX11 201103L  //!< Value for the C++11 standard
#define KHAOS_STANDARD_CXX14 201402L  //!< Value for the C++14 standard
#define KHAOS_STANDARD_CXX17 201703L  //!< Value for the C++17 standard
#define KHAOS_STANDARD_CXX20 202002L  //!< Value for the C++20 standard

#endif
